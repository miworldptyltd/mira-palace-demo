// Mira Palace — vanilla JS
(function () {
  'use strict';

  // ---- Header solidify on scroll -----------------------------------------
  const header = document.getElementById('siteheader');
  if (header) {
    const update = () => header.classList.toggle('is-scrolled', window.scrollY > 24);
    update();
    window.addEventListener('scroll', update, { passive: true });
  }

  // ---- Mobile menu toggle ------------------------------------------------
  const menuBtn = document.getElementById('mnu-toggle');
  const menu = document.getElementById('mobile-menu');
  if (menuBtn && menu) {
    menuBtn.addEventListener('click', () => {
      const open = menu.classList.toggle('hidden') === false;
      menuBtn.setAttribute('aria-expanded', open ? 'true' : 'false');
    });
    menu.querySelectorAll('a').forEach((a) => a.addEventListener('click', () => menu.classList.add('hidden')));
  }

  // ---- Light-touch image protection (deters casual right-click save) ----
  document.addEventListener('contextmenu', (e) => {
    if (e.target && e.target.tagName === 'IMG') e.preventDefault();
  });

  // ---- Smooth scroll for in-page anchors --------------------------------
  document.querySelectorAll('a[href^="#"]').forEach((a) => {
    a.addEventListener('click', (e) => {
      const id = a.getAttribute('href').slice(1);
      const el = id && document.getElementById(id);
      if (el) {
        e.preventDefault();
        el.scrollIntoView({ behavior: 'smooth', block: 'start' });
      }
    });
  });

  // ---- Hero video & music selectors (radically simplified) ---------------
  const videoEl   = document.getElementById('hero-video');
  const videoSrc  = document.getElementById('hero-video-source');
  const audio     = document.getElementById('ambient-audio');
  const musicToggle = document.getElementById('music-toggle');
  const musicLabel  = document.getElementById('music-label');

  // Video & music maps come from media-manifest.js (auto-generated by build.py
  // by scanning site/assets/{video,audio}/). If the manifest didn't load for
  // any reason, fall back to a tiny safe default.
  const VIDEOS = (window.MIRA_VIDEOS && Object.keys(window.MIRA_VIDEOS).length)
    ? window.MIRA_VIDEOS
    : { none: '' };
  const TRACKS = (window.MIRA_TRACKS && Object.keys(window.MIRA_TRACKS).length)
    ? window.MIRA_TRACKS
    : { none: '' };
  const DEFAULT_VIDEO = window.MIRA_DEFAULT_VIDEO || (Object.keys(VIDEOS).find(k => k !== 'none') || 'none');
  const DEFAULT_TRACK = window.MIRA_DEFAULT_TRACK || (Object.keys(TRACKS).find(k => k !== 'none') || 'none');

  // Project root prefix. Python's render_page() writes the right value as
  // window.MIRA_ROOT (or body[data-root]) for every page, so we use that.
  // Fall back to the videoEl's data-root or a path-counting heuristic only
  // if both globals are missing (e.g. someone hand-edited an HTML file).
  // CRITICAL: empty string IS a valid value (means "root"), so check for
  // 'undefined' explicitly rather than truthiness.
  let ROOT;
  if (typeof window.MIRA_ROOT !== 'undefined') {
    ROOT = window.MIRA_ROOT;
  } else if (document.body && typeof document.body.dataset.root !== 'undefined') {
    ROOT = document.body.dataset.root;
  } else if (videoEl && typeof videoEl.dataset.root !== 'undefined') {
    ROOT = videoEl.dataset.root;
  } else {
    const segs = location.pathname.split('/').filter(Boolean);
    const depth = segs.length - (segs[segs.length - 1] && segs[segs.length - 1].includes('.') ? 1 : 0);
    ROOT = '../'.repeat(Math.max(0, depth));
  }

  // ----- Video swap (home-page only — videoEl exists) ---------------------
  function applyVideo(key) {
    if (!videoEl || !videoSrc) return;
    const path = VIDEOS[key] || '';
    if (!path) { videoEl.style.display = 'none'; try { videoEl.pause(); } catch(e){}; return; }
    videoEl.style.display = '';
    videoSrc.src = ROOT + path;
    videoEl.load();
    videoEl.play().catch(() => {});
  }

  // ----- Music: preload the build-time default track on every page --------
  // The track path is whichever default the manifest specified (typically
  // ambient). If the user picked a different track on a previous visit we
  // honour that — but only if the key is still valid (file still on disk).
  let savedTrackKey = DEFAULT_TRACK;
  try {
    const t = localStorage.getItem('mp-music');
    if (t && t in TRACKS && TRACKS[t]) savedTrackKey = t;
  } catch (e) {}

  // Cross-page continuity. Persisted in sessionStorage (per-tab) as
  // {key, time, playing}. If the previous page was playing the same track,
  // resume from where it left off rather than restarting at zero.
  const SS_MUSIC = 'mp-music-state';
  let sessState = null;
  try {
    const raw = sessionStorage.getItem(SS_MUSIC);
    sessState = raw ? JSON.parse(raw) : null;
  } catch (e) {}
  if (sessState && sessState.key && sessState.key in TRACKS && TRACKS[sessState.key]) {
    savedTrackKey = sessState.key;
  }

  // Set the audio source for this page (the picker can swap it later).
  if (audio && !audio.src && TRACKS[savedTrackKey]) {
    audio.src = ROOT + TRACKS[savedTrackKey];
    audio.load();  // explicit load so metadata is fetched even if preload is 'none'
  }

  // If the session said music was playing, seek to the saved time and resume.
  // Browsers preserve "user activation" across same-origin navigations, so
  // autoplay works once the user has clicked ♪ at least once. If a browser
  // still blocks it we fall back to the manual play button gracefully.
  const wantResume = audio && sessState && sessState.playing
    && sessState.key === savedTrackKey && TRACKS[savedTrackKey];
  if (wantResume) {
    const seek = Math.max(0, Number(sessState.time) || 0);
    let resumed = false;
    const startResume = () => {
      if (resumed) return;
      resumed = true;
      // Two-phase: seek FIRST, then start playing once the seek lands.
      // Otherwise the browser may play a fraction of audio from byte zero
      // while the seek is still being satisfied.
      const playNow = () => {
        audio.play().then(() => {
          intentPlaying = true;
          if (musicLabel) musicLabel.textContent = 'Music on';
        }).catch(() => {
          // Autoplay blocked — leave intent so one tap on ♪ resumes.
          if (musicLabel) musicLabel.textContent = 'Click ♪ to resume';
        });
      };
      if (seek > 0 && seek < (audio.duration || Infinity)) {
        let landed = false;
        const onSeeked = () => { if (landed) return; landed = true; audio.removeEventListener('seeked', onSeeked); playNow(); };
        audio.addEventListener('seeked', onSeeked);
        // Safety net — if 'seeked' never fires (some edge cases) start
        // playing anyway after 400ms.
        setTimeout(() => onSeeked(), 400);
        try { audio.currentTime = seek; } catch (e) { onSeeked(); }
      } else {
        playNow();
      }
    };
    // readyState >= 1 means metadata is already known (duration etc.).
    if (audio.readyState >= 1) {
      startResume();
    } else {
      audio.addEventListener('loadedmetadata', startResume, { once: true });
      // 'canplay' is a safety net for browsers that skip 'loadedmetadata'
      // when the file comes from cache.
      audio.addEventListener('canplay', startResume, { once: true });
    }
  }

  // Save state continuously (throttled) and on unload. Intent is tracked
  // SEPARATELY from audio.paused — when the page navigates away the browser
  // auto-pauses the element and fires 'pause', which we deliberately ignore
  // so the next page knows the user still WANTS music.
  let intentPlaying = !!(sessState && sessState.playing);
  function saveMusicState() {
    if (!audio) return;
    try {
      const src = audio.currentSrc || audio.src || '';
      let k = '';
      for (const key in TRACKS) {
        if (TRACKS[key] && src.endsWith(TRACKS[key])) { k = key; break; }
      }
      if (!k) return;
      sessionStorage.setItem(SS_MUSIC, JSON.stringify({
        key: k,
        time: audio.currentTime || 0,
        playing: intentPlaying,
      }));
    } catch (e) {}
  }
  if (audio) {
    let _lastSave = 0;
    audio.addEventListener('timeupdate', () => {
      const now = Date.now();
      if (now - _lastSave > 900) { saveMusicState(); _lastSave = now; }
    });
    // Save just before the page unloads. Use both events to cover Safari
    // (pagehide) and Chromium (visibilitychange + beforeunload). Don't listen
    // to audio's own play/pause — they fire during navigation tear-down.
    window.addEventListener('pagehide',     saveMusicState);
    window.addEventListener('beforeunload', saveMusicState);
    document.addEventListener('visibilitychange', () => {
      if (document.visibilityState === 'hidden') saveMusicState();
    });
  }

  // Default-highlight for video — use saved choice if still valid, else default.
  let savedVid = DEFAULT_VIDEO;
  try {
    const v = localStorage.getItem('mp-video');
    if (v && v in VIDEOS) savedVid = v;
  } catch (e) {}
  applyVideo(savedVid);

  document.querySelectorAll('.vid-btn').forEach(b => b.dataset.active = (b.dataset.video === savedVid ? 'true' : 'false'));
  document.querySelectorAll('.mus-btn').forEach(b => b.dataset.active = (b.dataset.music === savedTrackKey ? 'true' : 'false'));

  // Video picker click
  document.querySelectorAll('.vid-btn').forEach(b => {
    b.addEventListener('click', () => {
      const k = b.dataset.video;
      applyVideo(k);
      try { localStorage.setItem('mp-video', k); } catch (e) {}
      document.querySelectorAll('.vid-btn').forEach(x => x.dataset.active = (x === b ? 'true' : 'false'));
    });
  });

  // Music picker click — swaps source AND auto-plays so the user hears the choice
  document.querySelectorAll('.mus-btn').forEach(b => {
    b.addEventListener('click', () => {
      const k = b.dataset.music;
      const path = TRACKS[k] || '';
      document.querySelectorAll('.mus-btn').forEach(x => x.dataset.active = (x === b ? 'true' : 'false'));
      // Remember the choice across pages.
      try { localStorage.setItem('mp-music', k); } catch (e) {}
      if (!audio) return;
      // Explicit pick resets the "tried and failed" list so the user can revisit
      // anything they previously skipped past.
      audio.dataset.attempted = k;
      if (!path) {
        try { audio.pause(); audio.removeAttribute('src'); } catch (e) {}
        intentPlaying = false;
        saveMusicState();
        if (musicLabel) musicLabel.textContent = 'Music off';
        return;
      }
      audio.src = ROOT + path;
      audio.load();
      audio.play().then(() => {
        intentPlaying = true;
        saveMusicState();
        if (musicLabel) musicLabel.textContent = 'Music on';
      }).catch((err) => {
        if (musicLabel) musicLabel.textContent = 'Click ♪ to play';
        console.warn('Audio play failed (auto):', err && err.message);
      });
    });
  });

  // ----- Auto-fallback: if a track fails to load, try the next valid one ---
  // Some files in the demo set may have wrong extensions or unsupported
  // codecs. Rather than alerting, we silently advance to the next track.
  function trackKeysInOrder() {
    return Object.keys(TRACKS).filter(k => k !== 'none' && TRACKS[k]);
  }
  function attemptedKeys() { return audio.dataset.attempted ? audio.dataset.attempted.split(',') : []; }
  function markAttempted(k) {
    const list = attemptedKeys();
    if (!list.includes(k)) list.push(k);
    audio.dataset.attempted = list.join(',');
  }
  function currentKeyFromSrc() {
    const src = audio.currentSrc || audio.src || '';
    const all = Object.keys(TRACKS);
    return all.find(k => TRACKS[k] && src.endsWith(TRACKS[k])) || '';
  }
  function tryNextTrack() {
    if (!audio) return;
    const order = trackKeysInOrder();
    const tried = attemptedKeys();
    const next = order.find(k => !tried.includes(k));
    if (!next) {
      if (musicLabel) musicLabel.textContent = 'No playable track';
      return;
    }
    markAttempted(next);
    document.querySelectorAll('.mus-btn').forEach(b => b.dataset.active = (b.dataset.music === next ? 'true' : 'false'));
    audio.src = ROOT + TRACKS[next];
    audio.load();
    audio.play().then(() => {
      intentPlaying = true;
      saveMusicState();
      if (musicLabel) musicLabel.textContent = 'Music on';
      try { localStorage.setItem('mp-music', next); } catch (e) {}
    }).catch(() => { /* error handler will retry */ });
  }
  if (audio) {
    audio.addEventListener('error', () => {
      const k = currentKeyFromSrc();
      if (k) markAttempted(k);
      console.warn('Audio error on', audio.currentSrc, '- trying next track');
      tryNextTrack();
    });
  }

  // Music play/pause floating button — ALWAYS reacts to a user click
  if (musicToggle && audio && musicLabel) {
    musicToggle.addEventListener('click', () => {
      // If somehow there's no src, force the build-time default track.
      if (!audio.src || audio.src === location.href) {
        const fallback = TRACKS[DEFAULT_TRACK] || '';
        if (fallback) {
          audio.src = ROOT + fallback;
          audio.load();
        }
      }
      if (audio.paused) {
        audio.play().then(() => {
          intentPlaying = true;
          saveMusicState();
          musicLabel.textContent = 'Music on';
          const k = currentKeyFromSrc();
          if (k) markAttempted(k);
        }).catch((err) => {
          musicLabel.textContent = 'Trying next track...';
          console.warn('Audio play failed:', err && err.message);
          tryNextTrack();
        });
      } else {
        audio.pause();
        intentPlaying = false;
        saveMusicState();
        musicLabel.textContent = 'Music off';
      }
    });
  }

  // ----- Music prev / next (cluster bubble buttons on every page) ---------
  function playTrackKey(key) {
    if (!audio || !TRACKS[key]) return;
    document.querySelectorAll('.mus-btn').forEach(x => x.dataset.active = (x.dataset.music === key ? 'true' : 'false'));
    audio.dataset.attempted = key;
    audio.src = ROOT + TRACKS[key];
    audio.load();
    const friendly = (window.MIRA_TRACK_LABELS && window.MIRA_TRACK_LABELS[key]) || key;
    audio.play().then(() => {
      intentPlaying = true;
      saveMusicState();
      if (musicLabel) {
        // Briefly show the track name on prev/next, then revert.
        musicLabel.textContent = friendly;
        clearTimeout(musicLabel._revertTimer);
        musicLabel._revertTimer = setTimeout(() => { musicLabel.textContent = 'Music on'; }, 2500);
      }
    }).catch((err) => {
      console.warn('Audio play failed:', err && err.message);
      if (musicLabel) musicLabel.textContent = 'Click ♪ to play';
    });
    try { localStorage.setItem('mp-music', key); } catch (e) {}
  }
  function cycleMusic(direction) {
    const keys = Object.keys(TRACKS).filter(k => k !== 'none' && TRACKS[k]);
    if (!keys.length) return;
    const cur = currentKeyFromSrc();
    let idx = keys.indexOf(cur);
    if (idx < 0) idx = (direction > 0 ? -1 : 0);
    const next = (idx + direction + keys.length) % keys.length;
    playTrackKey(keys[next]);
  }
  const musicPrev = document.getElementById('music-prev');
  const musicNext = document.getElementById('music-next');
  if (musicPrev) musicPrev.addEventListener('click', (e) => { e.stopPropagation(); cycleMusic(-1); });
  if (musicNext) musicNext.addEventListener('click', (e) => { e.stopPropagation(); cycleMusic(1); });

  // ----- Video cluster (home page only — hides itself elsewhere) -----------
  const videoCluster = document.getElementById('video-cluster');
  const videoToggle  = document.getElementById('video-toggle');
  const videoPrev    = document.getElementById('video-prev');
  const videoNext    = document.getElementById('video-next');
  const videoLabel   = document.getElementById('video-label');
  if (videoCluster) {
    if (!videoEl) {
      // No hero video on this page — hide the whole cluster.
      videoCluster.style.display = 'none';
    } else {
      const labelFor = (k) => {
        if (k === 'none') return 'Video off';
        return (window.MIRA_VIDEO_LABELS && window.MIRA_VIDEO_LABELS[k]) || 'Video';
      };
      const setVid = (k) => {
        savedVid = k;
        applyVideo(k);
        try { localStorage.setItem('mp-video', k); } catch (e) {}
        document.querySelectorAll('.vid-btn').forEach(x => x.dataset.active = (x.dataset.video === k ? 'true' : 'false'));
        if (videoLabel) videoLabel.textContent = labelFor(k);
      };
      const cycleVideo = (direction) => {
        const keys = Object.keys(VIDEOS); // include 'none' so users can turn it off
        let idx = keys.indexOf(savedVid);
        if (idx < 0) idx = 0;
        const next = (idx + direction + keys.length) % keys.length;
        setVid(keys[next]);
      };
      // Initial label
      if (videoLabel) videoLabel.textContent = labelFor(savedVid);
      if (videoPrev) videoPrev.addEventListener('click', (e) => { e.stopPropagation(); cycleVideo(-1); });
      if (videoNext) videoNext.addEventListener('click', (e) => { e.stopPropagation(); cycleVideo(1); });
      if (videoToggle) videoToggle.addEventListener('click', (e) => { e.stopPropagation(); cycleVideo(1); });
    }
  }

  // ----- Top-nav dropdowns: hover with grace period, tap on touch ----------
  const ddHoverCapable = window.matchMedia
    && window.matchMedia('(hover: hover) and (pointer: fine)').matches;
  document.querySelectorAll('.nav-dd').forEach(dd => {
    let closeTimer = 0;
    const open  = () => { clearTimeout(closeTimer); dd.dataset.open = 'true'; };
    const close = () => { clearTimeout(closeTimer); closeTimer = setTimeout(() => { dd.dataset.open = 'false'; }, 350); };
    if (ddHoverCapable) {
      dd.addEventListener('mouseenter', open);
      dd.addEventListener('mouseleave', close);
    }
    // Tap on the trigger toggles for touch users; on hover devices a tap
    // still lets the link navigate to the section's primary page.
    const trigger = dd.querySelector('.nav-dd-trigger');
    if (trigger && !ddHoverCapable) {
      trigger.addEventListener('click', (e) => {
        // First tap opens the menu, second tap navigates.
        if (dd.dataset.open !== 'true') {
          e.preventDefault();
          dd.dataset.open = 'true';
        }
      });
    }
  });
  // Tap outside any dropdown closes them all.
  document.addEventListener('click', (e) => {
    document.querySelectorAll('.nav-dd').forEach(dd => {
      if (!dd.contains(e.target)) dd.dataset.open = 'false';
    });
  });

  // ---- Customiser: theme + language --------------------------------------
  const cust = document.getElementById('customiser');
  if (cust) {
    const toggle = document.getElementById('cust-toggle');
    const panel  = document.getElementById('cust-panel');

    const setOpen = (open) => {
      cust.dataset.open = open ? 'true' : 'false';
      if (toggle) toggle.setAttribute('aria-expanded', open ? 'true' : 'false');
    };
    setOpen(false);

    // Hover behaviour for mouse / trackpad users — open on enter, close on
    // leave. Touch devices (no hover capability) keep the click toggle.
    // We attach listeners to the TAB and the PANEL individually rather than
    // the wrapper, because the wrapper's flex box still reserves layout
    // space for the (off-screen) panel — hovering that ghost area would
    // otherwise pop the panel open before the user actually touches the tab.
    const hoverCapable = window.matchMedia
      && window.matchMedia('(hover: hover) and (pointer: fine)').matches;
    if (hoverCapable) {
      let closeTimer = 0;
      const openNow  = () => { clearTimeout(closeTimer); setOpen(true); };
      const closeSoon = () => { clearTimeout(closeTimer); closeTimer = setTimeout(() => setOpen(false), 220); };
      // Hovering the tab opens the panel.
      toggle.addEventListener('mouseenter', openNow);
      toggle.addEventListener('mouseleave', closeSoon);
      // Hovering the panel keeps it open; leaving the panel closes after grace.
      panel.addEventListener('mouseenter', openNow);
      panel.addEventListener('mouseleave', closeSoon);
    }

    // Click on the tab works on every device — essential for touch.
    toggle.addEventListener('click', (e) => {
      e.stopPropagation();
      setOpen(cust.dataset.open !== 'true');
    });
    // Tap outside the customiser closes it (touch users).
    document.addEventListener('click', (e) => {
      if (!cust.contains(e.target)) setOpen(false);
    });

    // Apply saved theme on load
    let savedTheme = 'default';
    try { savedTheme = localStorage.getItem('mp-theme') || 'default'; } catch (e) {}
    if (savedTheme && savedTheme !== 'default') {
      document.documentElement.setAttribute('data-theme', savedTheme);
    }
    document.querySelectorAll('.theme-swatch').forEach((b) => {
      const isActive = b.dataset.theme === savedTheme;
      b.dataset.active = isActive ? 'true' : 'false';
      b.addEventListener('click', () => {
        const t = b.dataset.theme;
        if (t === 'default') {
          document.documentElement.removeAttribute('data-theme');
        } else {
          document.documentElement.setAttribute('data-theme', t);
        }
        try { localStorage.setItem('mp-theme', t); } catch (e) {}
        document.querySelectorAll('.theme-swatch').forEach((x) => x.dataset.active = (x === b ? 'true' : 'false'));
      });
    });

    // Language buttons (stub for non-EN)
    document.querySelectorAll('.lang-btn').forEach((b) => {
      b.addEventListener('click', () => triggerLang(b.dataset.lang, b));
    });
  }

  // Apply EN/TR copyright text. The site has English and Turkish copyright
  // strings rendered side-by-side; we toggle .lang-hidden so only one shows.
  // For DE/RU we don't have translated copyright — fall through to English.
  function applyCopyrightLang(lang) {
    const effective = (lang === 'tr') ? 'tr' : 'en';
    document.querySelectorAll('.lang-text').forEach((el) => {
      const elLang = el.dataset.lang;
      if (elLang === effective) el.classList.remove('lang-hidden');
      else el.classList.add('lang-hidden');
    });
  }
  // Restore previously selected language on page load (before any clicks).
  let initialLang = 'en';
  try { initialLang = localStorage.getItem('mp-lang') || 'en'; } catch (e) {}
  applyCopyrightLang(initialLang);
  document.querySelectorAll('.lang-btn,.nav-flag').forEach((x) => {
    x.dataset.active = (x.dataset.lang === initialLang ? 'true' : 'false');
  });

  // Nav flag buttons + customiser language buttons drive the same handler.
  function triggerLang(lang, sourceBtn) {
    document.querySelectorAll('.lang-btn,.nav-flag').forEach((x) => {
      x.dataset.active = (x.dataset.lang === lang ? 'true' : 'false');
    });
    try { localStorage.setItem('mp-lang', lang); } catch (e) {}
    applyCopyrightLang(lang);
    if (lang === 'en' || lang === 'tr') {
      // Both languages have first-class copyright text on the site already —
      // no banner needed.
      const lm = document.getElementById('lang-msg');
      if (lm) { lm.classList.add('hidden'); lm.textContent = ''; }
      const oldBanner = document.getElementById('lang-banner');
      if (oldBanner) oldBanner.remove();
      return;
    }
    const messages = {
      de: 'Deutsche Version folgt — der Sprachumschalter funktioniert in diesem Mock; echte deutsche Inhalte folgen vor dem Launch.',
      ru: 'Русская версия скоро — переключатель языка работает в этом макете; настоящий русский контент будет добавлен перед запуском.'
    };
    let banner = document.getElementById('lang-banner');
    if (!banner) {
      banner = document.createElement('div');
      banner.id = 'lang-banner';
      banner.className = 'fixed top-20 left-1/2 -translate-x-1/2 z-40 bg-mira-900 text-white text-sm px-4 py-3 rounded-lg shadow-lux max-w-md text-center';
      banner.style.cursor = 'pointer';
      banner.title = 'Click to dismiss';
      banner.addEventListener('click', () => banner.remove());
      document.body.appendChild(banner);
      setTimeout(() => banner && banner.remove(), 6000);
    }
    banner.textContent = messages[lang] || '';
  }
  document.querySelectorAll('.nav-flag').forEach((b) => {
    b.addEventListener('click', () => triggerLang(b.dataset.lang, b));
  });
})();
